print("Cálculo da média")
nota1 = input("Digite a 1a. nota: ")
nota1 = float(nota1)
nota2 = input("Digite a 2a. nota: ")
nota2 = float(nota2)
nota3 = input("Digite a 3a. nota: ")
nota3 = float(nota3)
soma = nota1 + nota2 + nota3
media = soma / 3
print("Sua média foi:", media)
